namespace KarlBanan.EditorLayout
{
    /// <summary>
    /// Defines how an element should be aligned on the cross axis of a layout.
    /// </summary>
    /// <remarks>
    /// In a horizontal row, this controls vertical alignment.
    /// In a vertical column, this would control horizontal alignment if used there.
    /// </remarks>
    public enum CrossAxisAlignment
    {
        Top,
        Middle,
        Bottom
    }
}